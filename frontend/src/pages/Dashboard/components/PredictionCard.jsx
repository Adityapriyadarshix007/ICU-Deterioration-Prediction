import React, { useRef } from 'react';
import { motion } from 'framer-motion';
import { Calendar, ChevronRight, Download, Printer, FileText, TrendingUp, TrendingDown, AlertCircle } from 'lucide-react';
import Card from '../../../components/ui/Card';
import Button from '../../../components/ui/Button';
import StatusBadge from '../../../components/ui/StatusBadge';
import RiskGauge from './RiskGauge';
import SHAPExplanation from './SHAPExplanation';
import { getRiskColor, getRecommendation, getAlertLevel } from '../utils/helpers';
import { exportStructuredPDF } from '../../../utils/pdfExport';
import toast from 'react-hot-toast';

const PredictionCard = ({ prediction, onExport, onPrint }) => {
  const cardRef = useRef(null);
  
  if (!prediction) return null;
  
  const riskScore = typeof prediction.risk_score === 'number' ? prediction.risk_score : 0.5;
  const riskPercentage = Math.round(Math.max(0, Math.min(100, riskScore * 100)));
  
  let confidence = 0.85;
  if (typeof prediction.confidence === 'number') {
    confidence = Math.max(0, Math.min(1, prediction.confidence));
  } else if (typeof prediction.confidence === 'string') {
    const confidenceMap = { 'HIGH': 0.95, 'MEDIUM': 0.85, 'LOW': 0.70 };
    confidence = confidenceMap[prediction.confidence] || 0.85;
  }
  const confidencePercentage = Math.round(confidence * 100);
  
  const ciMargin = 0.07;
  const ciLower = Math.max(0, Math.round((riskScore - ciMargin) * 100));
  const ciUpper = Math.min(100, Math.round((riskScore + ciMargin) * 100));
  
  const alertLevel = prediction.alert_level || getAlertLevel(riskPercentage);
  const rec = getRecommendation(riskPercentage);
  
  const threshold = prediction.threshold || 0.459;
  const thresholdPercentage = Math.round(Math.max(0, Math.min(100, threshold * 100)));
  const isAboveThreshold = riskScore > threshold;

  const handleExportPDF = async () => {
    try {
      toast.loading('Generating PDF...', { id: 'pdf-export' });
      
      // Prepare data for PDF
      const pdfData = {
        patient_name: prediction.patient_name,
        patient_id: prediction.patient_id,
        prediction_time: prediction.prediction_time,
        risk_score: prediction.risk_score,
        alert_level: prediction.alert_level,
        confidence: confidence,
        shap_values: prediction.shap_values,
        features: prediction.features,
      };
      
      const success = await exportStructuredPDF(pdfData);
      toast.dismiss('pdf-export');
      if (success) {
        toast.success('PDF exported successfully!');
      } else {
        toast.error('Failed to export PDF');
      }
    } catch (error) {
      toast.dismiss('pdf-export');
      toast.error('Failed to export PDF: ' + (error.message || 'Unknown error'));
      console.error('PDF export error:', error);
    }
  };

  const handlePrint = () => {
    window.print();
  };

  return (
    <motion.div
      ref={cardRef}
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      exit={{ opacity: 0, y: -20 }}
      className="mt-8"
    >
      <Card 
        title="Prediction Summary"
        icon={FileText}
        className={`border-l-8 ${
          alertLevel === 'CRITICAL' ? 'border-red-500' :
          alertLevel === 'WARNING' ? 'border-yellow-500' :
          'border-green-500'
        }`}
        actions={
          <div className="flex items-center gap-2">
            <Button variant="outline" size="sm" icon={Download} onClick={handleExportPDF}>
              Export PDF
            </Button>
            <Button variant="outline" size="sm" icon={Printer} onClick={handlePrint}>
              Print
            </Button>
          </div>
        }
      >
        {/* Clinical Disclaimer */}
        <div className="mb-4 p-3 bg-yellow-50 border border-yellow-200 rounded-lg">
          <p className="text-xs text-yellow-800 flex items-start gap-2">
            <AlertCircle className="w-4 h-4 text-yellow-600 flex-shrink-0 mt-0.5" />
            <span>Clinical Decision Support Notice: This prediction is generated by an AI model trained on retrospective ICU data 
            and is intended to assist, not replace, clinician judgment. Final treatment decisions should always be made 
            by qualified healthcare professionals.</span>
          </p>
        </div>

        <div className="grid grid-cols-1 lg:grid-cols-3 gap-6">
          <div>
            <div className="flex items-center gap-2 mb-2">
              <p className="text-sm text-gray-500">Patient</p>
              <StatusBadge 
                status={getRiskColor(alertLevel)}
                label={alertLevel}
                size="lg"
              />
            </div>
            <p className="text-lg font-semibold">{prediction.patient_name || 'Unknown'}</p>
            <p className="text-sm text-gray-500 font-mono">MRN: {prediction.patient_id || 'N/A'}</p>
            <div className="mt-2 flex items-center gap-2 text-xs text-gray-400">
              <Calendar className="w-3.5 h-3.5" />
              <span>Generated: {prediction.prediction_time ? new Date(prediction.prediction_time).toLocaleString() : 'Just now'}</span>
            </div>
            <div className="mt-1 flex items-center gap-2 text-xs text-gray-400">
              <span>Prediction Horizon: 24 hours</span>
            </div>
            <div className="mt-3 p-2 bg-gray-50 rounded-lg border border-gray-200">
              <div className="flex items-center justify-between">
                <span className="text-xs text-gray-500">Decision Threshold</span>
                <span className="text-xs font-semibold text-gray-700">{thresholdPercentage}%</span>
              </div>
              <div className="flex items-center justify-between mt-1">
                <span className="text-xs text-gray-500">Risk Status</span>
                <span className={`text-xs font-semibold ${isAboveThreshold ? 'text-red-600' : 'text-green-600'}`}>
                  {isAboveThreshold ? '⚠️ Above Threshold' : '✅ Below Threshold'}
                </span>
              </div>
            </div>
          </div>

          <div className="flex flex-col items-center justify-center">
            <RiskGauge 
              value={riskPercentage} 
              size={140}
              label="Risk Score"
            />
            <div className="mt-2 flex flex-col items-center gap-1">
              <div className="flex items-center gap-4 text-xs text-gray-500">
                <span>Confidence: {confidencePercentage}%</span>
              </div>
              <div className="flex items-center gap-2 text-xs text-gray-400">
                <span>95% CI: {ciLower}% – {ciUpper}%</span>
              </div>
            </div>
          </div>

          <div>
            <p className="text-sm font-medium text-gray-700">{rec.title}</p>
            <ul className="mt-2 space-y-1.5">
              {rec.actions.map((action, idx) => (
                <li key={idx} className="flex items-start gap-2 text-sm text-gray-600">
                  <ChevronRight className="w-3.5 h-3.5 text-blue-500 mt-0.5 flex-shrink-0" />
                  <span>{action}</span>
                </li>
              ))}
            </ul>
          </div>
        </div>

        {prediction.shap_values && Object.keys(prediction.shap_values).length > 0 && (
          <div className="mt-6 pt-6 border-t border-gray-200">
            <div className="flex items-center gap-2 mb-4">
              <span className="text-sm font-semibold text-gray-700">Top Contributing Clinical Factors</span>
              <span className="text-xs text-gray-400">SHAP Explainability</span>
            </div>
            <SHAPExplanation shapValues={prediction.shap_values} />
          </div>
        )}

        {(!prediction.shap_values || Object.keys(prediction.shap_values).length === 0) && (
          <div className="mt-6 pt-6 border-t border-gray-200">
            <div className="flex items-center gap-2 mb-2">
              <span className="text-sm font-semibold text-gray-700">Top Contributing Factors</span>
            </div>
            <div className="flex flex-wrap gap-2">
              {prediction.features && Object.entries(prediction.features).map(([key, value]) => {
                const ranges = {
                  heart_rate: { high: 100, low: 60 },
                  sbp: { high: 140, low: 90 },
                  dbp: { high: 90, low: 60 },
                  gcs: { high: 15, low: 13 },
                  lactate: { high: 2.0, low: 0.5 },
                  urine_output: { high: 60, low: 30 },
                  fio2: { high: 40, low: 21 },
                  creatinine: { high: 1.3, low: 0.6 }
                };
                const range = ranges[key];
                if (!range) return null;
                const numValue = parseFloat(value);
                if (isNaN(numValue)) return null;
                
                let status = 'normal';
                let icon = null;
                let color = 'text-gray-500';
                
                if (numValue > range.high) {
                  status = 'high';
                  icon = <TrendingUp className="w-3 h-3 text-red-500" />;
                  color = 'text-red-600';
                } else if (numValue < range.low) {
                  status = 'low';
                  icon = <TrendingDown className="w-3 h-3 text-blue-500" />;
                  color = 'text-blue-600';
                }
                
                if (status === 'normal') return null;
                
                const labels = {
                  heart_rate: 'Heart Rate',
                  sbp: 'Systolic BP',
                  dbp: 'Diastolic BP',
                  gcs: 'GCS',
                  lactate: 'Lactate',
                  urine_output: 'Urine Output',
                  fio2: 'FiO₂',
                  creatinine: 'Creatinine'
                };
                
                return (
                  <div key={key} className={`flex items-center gap-1.5 px-3 py-1.5 bg-gray-50 rounded-lg border border-gray-200 ${color}`}>
                    {icon}
                    <span className="text-xs font-medium">{labels[key] || key}</span>
                    <span className="text-xs">{numValue.toFixed(1)}</span>
                    <span className={`text-xs ${status === 'high' ? 'text-red-500' : 'text-blue-500'}`}>
                      {status === 'high' ? '↑' : '↓'}
                    </span>
                  </div>
                );
              })}
            </div>
          </div>
        )}
      </Card>
    </motion.div>
  );
};

export default PredictionCard;
